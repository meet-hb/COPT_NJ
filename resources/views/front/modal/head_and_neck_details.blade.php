                <h5>Headaches and Migraines</h5>
                <p>


                    Over 45 million Americans suffer from headaches. There are many different types of headaches and
                    migraines from tension headaches, temporal headaches, cluster headaches, migraines and sinus
                    headaches. While there are many different “triggers” such as alcohol, chocolate, scents and other
                    factors for headaches and migraines, most suffers do have some underlying neck problem that causes
                    their headaches. These are called “cervico-genic headaches”. <br>

                    Headaches and migraines are often spasms of the arteries in the scalp or base of the neck triggering
                    a whirlwind of abnormal electrical activity in the brain. This can cause a variety of symptoms
                    depending on where this is occurring in the head. Most headaches have a component of stress. When
                    stressed, our neck muscles tense on a chronic basis, and with supporting our head all day long,
                    become very irritated. Along with other triggers, this can start a headache or migraine. It is why
                    many headache and migraine sufferers have symptoms later on in the day.</p>

                <h5>
                    How physical therapy helps
                </h5>
