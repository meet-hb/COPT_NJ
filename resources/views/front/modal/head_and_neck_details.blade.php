                <h5>About Vertigo</h5>
                <p>Vertigo and dizziness can be the result of a variety of different factors. Vertigo or benign
                    paroxysmal positional vertigo (BPPV) can occur because of calcium crystals that form in the inner
                    ear labyrinth canals. These canals give feedback about balance to your brain.
                </p>
                <h5>About Dizziness</h5>
                <p>Dizziness can occur for a variety of reasons, including issues with medication. Issues with mobility
                    of the neck joints and muscles can also affect dizziness. When this occurs, the major arteries in
                    the neck which supply the back portion of the brain that control balance and orientation can be
                    affected.</p>
                <p>It is important to note that just because your pain is felt in one location, that is not usually the
                    source of the trouble. For example pain on the outside of the shoulder can be from an impingement of
                    the joint or problems with the rotator cuff muscles. However, this can be caused by poor positioning
                    and functioning of the shoulder blade, which is the real culprit. Treat the source of the problem
                    and the irritation will resolve.</p>
                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Our physical therapists perform a thorough examination of your balance and vestibular symptoms. We
                    evaluate your visual movements, positioning of your head, neck mobility and balance. At times, we
