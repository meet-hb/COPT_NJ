                <h5>What is Forward Head Syndrome?</h5>
                <p>
                    Forward head syndrome is extremely common in today’s society and describes a condition where, due to
                    changes in posture, the head juts forward actually shortening the neck. This can be mild or severe
                    and is affected by different factors such as type of work, age, arthritis and genetic factors.

                </p>

                <p>Due to prolonged sitting, poor posture with work, working at a computer, sitting at home in poor
                    posture watching TV and a host of other common poor postures, changes in your spine can occur over
                    time. Think about how much time you spend looking into a screen and hunched over a desk. The
                    10–12lbs of your head are pulling forward on your spine. <br> Over time, the normal curve in your
                    neck reduces, the length of the spine decreases and muscles in the neck become weaker. This can lead
                    to a variety of symptoms from neck pain, to headaches and even impinged nerves radiating pain into
                    the upper extremities.</p>

