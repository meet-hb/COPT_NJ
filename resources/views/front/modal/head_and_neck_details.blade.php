                <h5>What is Arthritis and Stenosis?</h5>
                <p>
                    As we age, we use our neck joints and muscles thousands of times a day. This wear and tear over time
                    can lead to bony changes in the joints and bones (vertebrae) of the neck. These bony changes, along
                    with poor posture, cause spurs of bone that can rub and irritate the surrounding tissue. <br>

                    When the spurs protrude in the holes on the side of the neck where the nerves exit out (foramen), or
                    the central spinal cord canal, this is called spinal stenosis. Spinal stenosis can be central
                    stenosis or foramenal stenosis. It can cause a variety of symptoms, from chronic pain, to numbness
                    and tingling into the arms. In severe cases of central canal stenosis, which affects the spinal
                    cord, poor balance, pain in the legs and difficulty with walking can occur.
                </p>
                <p>People with osteoarthritis in the neck can experience chronic pain, stiffness and limited range of
                    motion with turning their head and looking up. Pain may be worse in the morning or after prolonged
                    sitting. <br>

                    People with rheumatoid arthritis can suffer with neck pain. It is very important for people with
                    rheumatoid arthritis to maintain strength in their shoulder, neck and postural muscles. This is
                    because a very important ligament in the upper neck often weakens with rheumatoid arthritis and can
                    be a serious health risk if injured.</p>

                <h5>
                    How physical therapy helps
