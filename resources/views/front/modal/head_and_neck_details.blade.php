                <p>
                    There are many different reasons for neck pain. The neck requires good alignment, proper movement,
                    strength and stability to work correctly. The neck or cervical spine has to bear the weight of the
                    head, which is around 10-12lbs (the weight of a bowling ball). With injury or prolonged poor posture
                    (for example sitting at a computer), change in alignment, weakness and instability start to occur.
                    This irritates the structures of the neck and can even impinge on the nerves exiting the neck, or
                    the central canal protecting the spinal cord.</p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Our physical therapists perform a thorough evaluation of your neck to determine your range of
