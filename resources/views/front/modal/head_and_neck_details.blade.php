                <h5>What is Radiating Pain?</h5>
                <p>
                    If you are experiencing radiating pain to the shoulder, arm and even hands, the source of your
                    trouble could be your neck or “cervical spine”. The nerves that supply sensation and movement travel
                    from your neck down your arms. These nerves exit from your spine on both sides supplying the left
                    and right sides respectively. Often with poor posture, poor alignment, muscle weakness or injury,
                    the structure of the spine can change. This can lead to a narrowing of the holes where the different
                    nerves exit from your spinal cord, out of the neck to the shoulders and upper extremities. These
                    holes (foramen) depend on the proper alignment and movement of each level of joints in your neck.
                </p>

                <p>Symptoms often start off as a mild, occasional tingling to certain areas of the shoulder, arm or
                    hand. It can then worsen to pain and even numbness. This does require medical attention, so call us
                    for an evaluation immediately and we will coordinate with your physician. Most symptoms can be
                    reduced quickly. However, the time it takes to reduce symptoms depends on how long and to the extent
                    the symptoms have been occurring.</p>

