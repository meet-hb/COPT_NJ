                <h5>About Temporomandibular Joint Dysfunction (TMD)</h5>
                <p>Temporomandibular Joint Dysfunction (TMD) describes the problems that may occur at the
                    temporomandibular joint (TMJ). This joint is where your jaw bone attaches to the skull. It is a
                    complex joint that has to slide forward and drop down in order for your jaw to open. It moves
                    thousands of times a day with talking, eating and any time you move your jaw. The joint has a disc
                    or fluid filled sac within it and has many ligaments and muscles that coordinate movements. <br>

                    The TMJ relies heavily on proper posture and alignment to function well. The posture and positioning
                    of the neck and head play a large role in the pulls of muscles on the TMJ. Abnormal muscle pulls can
                    lead to altered movement of the TMJ and thus pain.</p>

