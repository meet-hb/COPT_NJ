                <h5>What is Degenerative Disc Disease?</h5>
                <p>
                    As a normal part of the aging process, the fluid filled discs between our neck bones (vertebrae) dry
                    out and shrink. However, in some individuals, this can be excessive, leading to a severe loss of
                    height in one or more sections of the neck. This loss of height can increase rubbing of bone on
                    bone, produce bone spurring, increase pressure on joints and nerves exiting the neck. <br>

                    Symptoms can range from chronic aching pain in the neck, to radiating pain in the arms, numbness /
                    tingling and headaches.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is a very important part in the maintenance of degenerative disc disease (DDD) and
                    degenerative joint disease (DJD). While the disease process itself can’t be changed, maintaining
                    good posture, joint mobility, alignment, strength and range of motion, can reduce or eliminate
                    symptoms and maintain a healthier spine. <br>

                    Our physical therapy is a great treatment solution to help you feel better and return to normal
                    activities. If symptoms do return in the future, know that we are here to help you resolve your pain
                    quickly. Call us today to discover how we can help you!
