                <h5>What is Whiplash?</h5>
                <p>


                    Whiplash can occur with many different accidents, but is often seen in motor vehicle accidents. The
                    weight of the head and spine is whipped back and forth with the fast movement of an accident or
                    fall. The muscles in the neck don’t have time to react and the harsh forces, strain, rip and tear
                    muscles, ligaments and other tissues. In severe cases, fractures can occur in the bones of the neck
                    (vertebrae). <br>

                    Due to the shock and trauma initially from an accident, pain may or may not be present. Whiplash
                    typically feels worse 1–2 days after the injury as inflammation sets in and tissues become swollen.
                    This can lead to headaches, pain with movement and even numbness / tingling into the upper
                    extremities. <br>

                    Often, people can have long standing weakness of the neck muscles, leading to future problems, if
                    their whiplash is not fully treated. Strengthening and postural exercises are very important to
                    maintain stability in the neck after a whiplash injury.</p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    There are different phases or treatment to help you after suffering whiplash. Our therapists will
                    work with your physician on the appropriate treatment to help you recover and relieve your pain. At
                    first very gentle hands on therapy, massage, ultrasound and electrical stimulation may be
                    administered to relieve the pain and gently help swollen tissues recover. <br>

                    Gradual progression is then made with restoring range of motion and beginning very gentle exercises.
                    As you progress, the pain reduces and your range of motion improves, strengthening and postural
                    exercises will be introduced to help you have a full recovery. It is very important to continue
                    through with the recommended treatment plan of your therapist to achieve a full recovery and prevent
                    future problems from occurring. Call us today to discover how we can help you relieve your whiplash
                    pain quickly and make a full recovery.
