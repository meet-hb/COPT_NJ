                    Our trained physical therapists examine for many different factors that can be adding to the causes
                    of your headache or migraine. They look for correct alignment, posture, muscle strength of the neck,
                    upper back and shoulders, joint movement in the neck and much more. <br>

                    If there are limitations in movement or strength in the neck or upper back, this will be addressed
                    to restore your normal mobility. This also increases blood flow in the area, reducing the common
                    spasms that can occur. In addition, our therapists will train you on exercises and techniques that
