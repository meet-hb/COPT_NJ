                <h5>About Osteoarthritis of the hip</h5>
                <p>Osteoarthritis of the hip can be painful as the hip is needed to move with sit to stand, walking,
                    squatting and bending. The hips take a lot of wear and tear over the years leading to a degeneration
                    of the cartilage that lines the joint. As the cartilage wears over time, the joint becomes stiffer
                    and the muscles of the buttocks generally weaken over time. This compounds the effects on the hip
                    causing grinding and wearing. In advanced stages, bony spurs can form around the joint and even
                    change the shape of the joint. <br>

                    Most minor to moderate cases of hip osteoarthritis can highly benefit from physical therapy. In
                    advanced stages a total or partial hip replacement may be needed to repair the damaged joint.
                    Physical therapy in the hospital and outpatient facilities is highly important in the recovery from
                    a hip replacement surgery.
                </p>

