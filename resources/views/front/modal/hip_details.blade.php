                <h5>What is Piriformis Syndrome?</h5>
                <p>
                    The piriformis muscle is deep in the buttocks and helps with rotating the hip. The sciatic nerve
                    typically dives underneath the piriformis muscle as it makes it way down to the leg. With excessive
                    sitting, loss of movement in the hips or trauma, the piriformis muscle can press down onto the
                    sciatic nerve. Typically, mild symptoms cause aching deep into the buttock and often radiating pain
                    to the outer thigh. With more severe cases, tingling, numbness or severe pain can radiate down the
                    thigh.
                </p>

                <h5>
                    How physical therapy helps
