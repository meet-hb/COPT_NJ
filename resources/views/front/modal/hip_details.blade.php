                <h5>What is Sciatica?</h5>
                <p>Sciatica is a term used to describe radiating pain into the buttock that may travel down the back of
                    the thigh. Often this pain is achy and spread out along the buttock and back of the thigh. <br>

                    Sciatica typically comes from irritation to the sciatic nerve, which travels deep in the buttock and
                    down the back of the leg. In about 20% of people, the sciatic nerve pierces through the piriformis
                    muscle deep in the buttock, instead of underneath it. This muscle helps guide hip movement, but can
                    become very tight with prolonged sitting. This tightens causes pressure and irritation to the
                    sciatic nerve causing pain. <br>

                    Radiating pain to the leg doesn’t necessarily mean you have sciatica, but it does tell you that
                    something is wrong. Irritated muscles and tissue often can radiate pain. Spread-out, achy pain is
                    often indicative of this type of problem. Sharp, stabbing pain with numbness or tingling is more
                    nerve irritation or compression occurring in the back or leg. This typically occurs more to specific
                    parts of the leg.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is one of the best treatments for sciatica and radiating pain into the leg. It
                    first takes a thorough evaluation to determine where your problem is stemming from. Our physical
                    therapists take time to examine the movement of your spine, hips and legs. Range of motion,
                    strength, joint mobility and muscle condition are assessed by our medically trained physical
                    therapists. <br>

                    After we determine the root cause of your problem, we generate a comprehensive treatment plan to
                    quickly relieve your pain, radiating symptoms, improve range of motion, improve strength and help
                    you to prevent future episodes. <br>

                    With gentle, specialized hands-on techniques we improve your spinal and hip mobility, reducing
                    pressure on your sciatic nerve. In addition, modalities such as heat, ice, electrical stimulation
                    and ultrasound may be used to reduce inflammation and resolve you pain quickly. We also perform
                    gentle stretching and strengthening exercises to restore your normal motion and strength, resulting
                    in lasting effects that will stop your pain from returning. Call us today to find out how we can
                    help relieve your sciatica and leg pain, returning you to the activities you love.
                </p>
