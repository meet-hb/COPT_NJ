                <h5>What is Trochanteric Bursitis?</h5>
                <p>The ending of the word “itis” is defined as inflammation. Therefore, bursitis is inflammation of a
                    bursa and tendonitis is inflammation of a tendon. A bursa is a fluid filled sac that sits between
                    muscles or tissues to cushion and reduce friction. In the hip there is a rather large bursa on the
                    outside between the bony area (tronchanter) and the thick band of tissue stretching from your hip to
                    your knee (iliotibial band). This is called the tronchanteric bursa. <br>
                    This bursa can often become inflamed due to abnormal joint movements, poor posture and weakness of
                    the surrounding musculature. This causes strain to the tissues and excessive friction on the bursa.
                    People tend to feel pain with prolonged walking or standing. It is often, very tender to touch on
                    the outer hip and thigh.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is the first line in conservative treatment for trochanteric bursitis. Since most
                    bursitis is due to underlying abnormal movement and weakness, our trained physical therapists
                    evaluate your movement to pinpoint the source of the trouble. Modalities may be used to alleviate
                    pain and discomfort, while hands-on therapy improves joint mechanics and range of motion. <br>

                    Finally, gentle strengthening exercises and joint coordination exercises help to restore stability
                    to the affected area and prevent re-occurrence of the symptoms. To find out more on how we can help
                    your hip bursitis call today!
                </p>
