                <h5>Hip Sprain / Strain</h5>
                <p>
                    Sprains refer to injuries of the ligaments (connect bone to bone) and strains refer to injuries of
                    the muscles or tendons (connect muscle to bone). Sprains and strains occur from quick
                    over-stretching of the tissues causing micro-tearing and subsequent injury. Swelling begins as part
                    of the inflammation process, causing pain and difficulty with movement. <br>

                    The first step in treating sprains or strains in the hip is to rest, ice and elevate it. With severe
                    limitations in movement you should see your physical therapist right away. There are different
                    levels of sprain or strain from mild to severe. In some cases, the tearing can be complete and even
                    be in need of surgical repair.</p>

