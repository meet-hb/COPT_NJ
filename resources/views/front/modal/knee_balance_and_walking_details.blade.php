                <h5>About Knee Pain</h5>
                <p>The knee is the most complex joint in the body. It has to bear the weight of the body while moving
                    through an incredible range of 130 degrees or more. When running the knee absorbs up to 6 times the
                    weight of your body in force! In a lifetime, it is estimated that the average person will take over
                    216 million steps and walk 108,000 miles. With this amount of use, at times things can go wrong and
                    lead to knee pain. <br>

                    Most knee pain stems from the loss of what is called “accessory motions”. Accessory motions are the
                    knee’s smaller movements that are sliding side-to-side, back and forth as well as spinning and
                    rotating. Without consistent stretching and especially without being very active, the tissues around
                    the knee become tight. In addition, if the muscles in the front or back of the knee become weaker,
                    that can lead to more abnormal forces on the knee joint. <br>

                    All of these problems lead to increased friction and wear on the knee. The normal response is one of
                    inflammation that can be felt as pain in and around the knee. A good rule of thumb is that if your
                    pain is achy, a lack of normal joint fluid flow is occurring as well as possible decreased
                    circulation around the knee to the muscles and tissues. However, with sharp pain during movement, an
                    area in the knee is being severely pressed upon and the movement of the joint is not normal.
                </p>

