                    to underlying abnormal mechanics of movement, walking and weakness. Our trained physical therapists
                    are experts in evaluating your movement to pinpoint the source of the trouble. Modalities may be
                    used to alleviate pain and discomfort, while hands-on therapy improves joint mechanics and movement.
                    <br>

                    Finally, gentle strengthening exercises and joint coordination exercises help to restore stability
