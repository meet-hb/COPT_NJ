                    injuries, poor alignment or weak musculature, the meniscus can become bruised and even torn. The
                    outside edges of the meniscus have more blood flow than the inner portions. This means, depending on
                    the area were the damage is located the healing process can be slow. <br>

                    Many times, meniscus injuries are mild to moderate and can be rehabilitated with physical therapy.
                    However, at times surgical intervention may be necessary to clean and shave down the torn areas of
                    the meniscus. Physical therapy is very important in the full recovery after this surgical procedure.

                </p>

