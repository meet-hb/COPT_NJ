                <h5>About Poor Balance / Risk of Falling</h5>
                <p>
                    Did you know that one out of three adults aged 65 and older fall each year? Among older adults,
                    falls are the leading cause of both fatal and nonfatal injuries. Our ability to balance when walking
                    or moving can change for a number of reasons. As we age, there is a natural loss of balance due to
                    slowing down of reflexes, muscle weakness and tissue changes. In addition, arthritis in the ankles,
                    knee or hips can affect balance. Furthermore, any neurological changes such as Parkinsons Disease,
                    spinal cord injuries, nerve injuries, back problems and much more can play a role in diminished
                    balance. <br>

                    Our ability to balance relies mainly on 3 factors. Our ability to perceive movement comes from the
                    nerve sensors in our joints and muscles, our vision and the position of our head in space through
                    the vestibular system in the inner ear. When any one of these factors is not optimum, our ability to
                    balance is affected and our risk for falling greatly increases. <br>

                    The good news is that there is a lot than can easily be done to improve your ability to balance,
                    which improves your walking and makes the risk of falling significantly less.

                </p>

