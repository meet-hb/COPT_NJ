                <h5>About Lateral Collateral Ligament (LCL) Tears</h5>
                <p>The lateral Collateral ligament (LCL) is an important ligament that checks the side to side sliding
                    of the femur bone on the tibia bone in the knee. This ligament can be injured with sports or falls,
                    especially with blows to the inside of the knee while the foot is planted on the ground. <br>

                    The LCL can be sprained which means it is still intact. If it is ruptured it means it is completely
                    broken. When a LCL injury involves a sprain, there is typically swelling that occurs in the knee and
                    a feeling of instability with walking. Sprains are classified according to their severity with
                    grades 1 through 3. Grades 1 and 2 are often treatable without surgery, while grade 3 is most often
                    a complete tear and typically requires surgery. <br>

                    With a sprain, the ligament is overstretched and micro-tearing results, causing pain and
                    inflammation. There is little blood flow to the ligaments and they get most of their nutrition from
                    the joint fluid. This means, that their healing is a lot slower than most other tissues. Depending
                    on the severity of the sprain and joint stability the potential for future injury can increase. <br>

                    With grade 3 LCL tears, surgery is most likely needed to repair. Physical therapy is vital to the
                    rehabilitation after this surgery. Recovery does take time and the goal is to protect the surgery
                    site and maintaining stability while getting back into walking and eventually running.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Our physical therapists work with patients of all ages, especially those who have sustained LCL
                    tears through sports activities. We work closely with your physician to examine the stability of
                    your knee after the LCL injury. There are specific tests that we perform to assess how stable the
                    ligament is and what course of action is best to resolve your pain and return you to activities as
                    soon as possible. Most grade 1 and grade 2 sprains can be rehabilitated without the need for surgery
                    and our physical therapists will discuss your options. <br>

                    If surgery is needed for your recovery, rest assured that you are in the right hands for your
                    rehabilitation. We work closely with your physician’s rehabilitation protocols. The priority is to
                    manage your pain and swelling after surgery and gradually increasing your range of motion in the
                    knee per your post-surgery protocol. <br>

                    As time progresses, we work with you to gain your strength back in the knee, improve stability of
                    the joint, get you back to walking normally and eventually back into advanced movements including
                    running. Call us today to discover how we can help you bounce back after a LCL tear.
