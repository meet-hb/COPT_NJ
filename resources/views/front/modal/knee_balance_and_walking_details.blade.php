                <h5>About Knee Tendonitis</h5>
                <p>The ending of the word “itis” is defined as inflammation. Therefore, tendonitis is inflammation of a
                    tendon, which connects muscles to bones. Commonly, the tendon that connects your quadriceps muscle
                    to the tibia bone (quadriceps tendon above the kneecap and patellar ligament below the kneecap) can
                    become inflamed resulting in a condition also known as jumper’s knee. This thick tendon runs over
                    the top of your kneecap and attaches to the tibia bone below. This structure can often become
                    inflamed due to abnormal joint movements, poor posture and weakness of the surrounding musculature.
                    This causes strain to the tendon with resulting pain during repetitive movement and especially with
                    squatting or kneeling down. Other areas of tendonitis in the knee can occur such as the back,
                    outside or inside of the knee.

                </p>

                <h5>
                    How physical therapy helps
                </h5>
