                <h5>What is Spondylolisthesis?</h5>
                <p>
                    Spondylolisthesis is a condition by where one spinal bone (vertebrae) is not in alignment with the
                    other. Typically, a slippage forward is found in the very low back at the 4th or 5th lumbar (low
                    back) vertebrae. This is due to a stretching out of the ligaments that hold the bones together or
                    possibly a fracture. With poor posture and weakened abdominals as well as spinal muscles, the
                    increase in the angle of the spine becomes too much over time and misalignment of the bones occurs.
                    <br>

                    Symptoms can be mild such as general fatigue to the low back, achy pain or severe symptoms if there
                    is compression on the nerves exciting the spine or spinal cord. There are varying degrees of
                    severity. Most fall into a mild to moderate category that can be stabilized with improved posture
                    and muscle strengthening. In severe cases, surgery may be necessary to fuse the area and bring
                    stability back.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy helps to bring stability and support to the low back pain allowing the muscles to
                    create more support for the overstretched ligaments. We work with you and your physician to create a
                    program that improves mobility in other spinal areas that may be inflexible, strengthen key muscles
                    to stabilize the spine and educate you on the proper techniques to maintain spinal stability. <br>

                    Other areas may need to be addressed such as hip weakness, poor balance or radiating symptoms to the
                    legs. Physical therapy can help treat the root source of these symptoms and get you back to feeling
