                    irritation and inflammation, which build up over time. Typically, low back pain suffers will have
                    more pain after sitting or lying down for prolonged periods, such as getting up from a chair or
                    first thing in the morning after sleeping. With severe pain, reaching or bending down for objects
                    can be limited. <br>

                    If pain is felt more with prolonged standing or walking, this can be a result of significant hip or
                    spinal weakness, again causing strain to the low back. With weakness in the spinal, abdominal or hip
                    muscles, the amount of force transferred to the back with everyday activities increases. With
                    bending down, the knees are often not used properly, and the muscles of the spine have to do extra
