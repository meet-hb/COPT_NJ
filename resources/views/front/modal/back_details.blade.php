                <h5>What is Spinal Arthritis?</h5>
                <p>
                    Spinal Arthritis is a very common area for osteoarthritis and rheumatoid arthritis. The low back
                    takes a tremendous strain throughout our lifetime, supporting the body, moving, sitting and
                    repetitively bending. In addition to these contributing factors, arthritis can also be affected by
                    genetics, age, previous injuries, diet and exercise. <br>

                    With abnormal forces on the back, the cartilage on the joints at each level of the spine can rub
                    down causing even wear, build up of bone and eventually bone on bone rubbing. This can result in
                    painful movement of the spinal joints and chronic, achy pain in the low back. Much like an arthritic
                    knee, the spinal joints are helped through restoring natural movement, improving support from the
                    spinal muscles and proper posture.</p>

                <h5>What is Spinal Stenosis?</h5>
                <p>
                    Spinal Stenosis is a condition that typically goes hand in hand with spinal arthritis. Spinal
                    stenosis refers to a narrowing of the central spinal canal or the canals where the nerves exit the
                    spine to the lower legs (called foramen). These canals are made up of overlapping spinal bones
                    (vertebrae) over another. With degeneration of the spinal joints, collapsing of the disc height or
                    abnormal bone growth, the canals can narrow. This leads to rubbing and even pressure on the nerves,
                    which can cause a multitude of symptoms.</p>

