                <h5>About Back Sprain / Strain</h5>
                <p>
                    It can be quite easy to sprain or strain your back. A sprain refers to an overstretching injury of
                    your ligaments in your spine, whereas a strain refers to an overstretching injury to your muscles.
                    There are hundreds of small muscles in the spine, which guide the intricate movements of each bone
                    and multi-level joints. This delicate ballet of movement can get into trouble when heavy lifting is
                    involved. <br>

                    The majority of people sprain or strain their back when they combine lifting with twisting. The best
                    way to avoid an injury to the low back is to use your legs when lifting, bending at the knees and
                    keeping your back fairly straight. If you have to turn while lifting, move your feet, otherwise the
                    strain of lifting while twisting can cause injury. <br>

                    Tearing of the tissues occurs during a sprain / strain. While this is often not a full complete
                    tear, the resulting damage can cause significant swelling and pain to occur deep in the back.
                    Typical healing times can take at least 6-12 weeks for complete healing.
                </p>

