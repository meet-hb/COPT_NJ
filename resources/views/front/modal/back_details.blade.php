                <h5>What is Sciatica?</h5>
                <p>
                    Sciatica is the term used to describe radiating pain into the buttock that can travel down the back
                    of the thigh. Often this pain is achy and spread out along these areas. Sciatica is a result of
                    irritation to the sciatic nerve, which travels deep in the buttock and down the back of the leg. In
                    about 20% of people, the sciatic nerve pierces through the piriformis muscle deep in the buttock
                    instead of under it. This can make the sciatic nerve more susceptible to irritation and pressure
                    from the piriformis muscle. The piriformis muscle helps guide hip movement, but can become very
                    tight with prolonged sitting. This increased tightness causes pressure and irritation to the sciatic
                    nerve causing pain.</p>

                <h5>What is Radiating Pain?</h5>
                <p>


                    Radiating pain to the leg doesn’t necessarily mean you have sciatica, but it does tell you that
                    something is wrong. Irritated muscles and tissue often can radiate pain. Spread-out, achy pain is
                    often indicative of this type of problem. Sharp, stabbing pain with numbness or tingling is more
                    nerve irritation or compression occurring in the low back or leg. These types of sensations
                    typically occur to specific parts of the leg.</p>

