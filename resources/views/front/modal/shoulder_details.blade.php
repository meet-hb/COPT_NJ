                <h5>About Labrum Tear</h5>
                <p>
                    The labrum is a thick ring of cartilage around the socket part of your shoulder joint (glenohumeral
                    joint). The labrum acts like a cup for the head of the humerus to sit in (like a ball inside a cup).
                    The labrum gives stability to the joint and also helps to cushion as the shoulder joint moves.
                </p>
                <p>The labrum can tear with injury from a blow to an outstretched arm or from repetitive injuries
                    overhead. Sometimes, a labrum tear can be involved when the rotator cuff is torn. A common tear is
                    called a SLAP lesion (Superior Labral tear from Anterior to Posterior). This is a tear of the labrum
                    from the top part in front to back. This often needs surgical repair and we work with your physician
                    on their protocol to rehab your shoulder after surgery.</p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Most often, labral tears are fixed surgically and need physical therapy after the procedure. We work
                    very closely with your physician to follow their protocols for rehabilitation to your shoulder.
                    Physical therapy involves a progress of your range of motion per the protocol and towards the end
                    restoring motion. In the meantime, pain, swelling and function are addressed. Call us today to find
                    out we can help you recover after a labral tear.
