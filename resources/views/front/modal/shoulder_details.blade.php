                <h5>Shoulder Pain</h5>
                <p>Dislocation of the shoulder typically occurs from falling onto an outstretched arm or a blow to the
                    side or back of the shoulder when falling on it. This can happen in different sports activities or
                    falls. Dislocations are managed medically to relocate the head of the humerus bone. Depending on the
                    severity of the dislocation, your physician will typically prescribe physical therapy to help
                    stabilize the shoulder joint and protect it during a recovery phase.</p>
                <p>With frequent dislocations, the shoulder can become unstable as many structures in the shoulder get
                    damaged and become too lax. By strengthening the muscles around the shoulder, stability can be
                    increased in the shoulder, preventing future dislocations.

                    At times, dislocations can be quite severe and lead to tearing of cartilage, tendons, ligaments or
                    muscles. In this case, surgery is often needed. After surgery physical therapy is an important part
                    of recovery and returning to normal activities.</p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is very important after a dislocation. Our physical therapists work with you
                    closely to protect the joint while it heals, teach you how to take care of your injury and gently
                    rehabilitate your shoulder. During the healing phase, your shoulder will be protected and pain will
                    be addressed.

                    Per your physician’s protocol we will gradually increase your range of motion, maintaining your
                    joint stability. Further into therapy, gentle strengthening is performed to improve the muscle
                    support around the shoulder. Ideally, full range of motion and strength is recovered allowing you to
                    return to normal activities and with the knowledge to protect your shoulder from further injury.
