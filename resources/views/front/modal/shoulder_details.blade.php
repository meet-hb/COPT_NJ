                <h5>About Shoulder Fractures</h5>
                <p>
                    Fractures in the shoulder occur for a variety of reasons, but typically from a fall onto the
                    shoulder itself. Fractures can occur in seniors also due to osteoporosis (thinning and weakening of
                    bone).
                </p>
                <p>
                    The goal with fracture management is to provide a safe position for the bone to heal (typically
                    around 8 weeks), while maintaining range of motion. As the bone heals, strengthening can begin and
                    rehabilitation back to normal activities is progressed.
                </p>
                <p>
                    At times surgery may be needed to hold the bone together with plates or screws. This stabilizes the
                    bone, but does disrupt muscles and leads to more weakness in the shoulder muscles. Physical therapy
                    is very much needed after this to restore normal range of motion and strength to the shoulder.
                    Recovery times can vary, but traditionally take 12–16 weeks.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is a very important part of rehabilitation after a shoulder fracture. Whether you
                    are a young person or older, we work closely with you and your physician to make sure your fracture
                    is protected while it heals. We then work closely with you to gradually restore your range of
                    motion, relieve pain, soothe aching muscles and improve your strength.
                </p>
                <p>
                    The goal of physical therapy is to return you to normal activities after the normal course of bone
                    healing. We can prevent long–term damage and address any issues that may have caused a fall onto the
                    arm in the first place. Call us today to learn more how we can help you after a fracture.
