                <h5>Shoulder Pain</h5>
                <p>The shoulder is the most complex joint in the human body. It has to move through more than 180
                    degrees of motion in many directions, rotate, slide and spin. There are a variety of muscles that
                    have to work in concert to ensure the shoulder joint tracks properly with everyday activities. It is
                    made up of the humerus bone, scapula (shoulder blade) and clavicle (collar bone). There are
                    technically 4 joints that make up the entire shoulder complex.</p>
                <p>Poor posture, weakened muscles, injury and lack of proper range of motion can all factor into
                    shoulder pain. Shoulder pain is typically felt in the muscles between the shoulder and neck, as well
                    as the outside shoulder radiating down. Where your pain is can determine what structures are
                    involved.</p>
                <p>It is important to note that just because your pain is felt in one location, that is not usually the
                    source of the trouble. For example pain on the outside of the shoulder can be from an impingement of
                    the joint or problems with the rotator cuff muscles. However, this can be caused by poor positioning
                    and functioning of the shoulder blade, which is the real culprit. Treat the source of the problem
                    and the irritation will resolve.</p>
                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    It is important that when you have shoulder pain, you have our experts evaluate your motion,
                    strength, coordination and joint mobility. By determining the root cause of your pain, we can then
                    treat it effectively for fast pain relief, improved motion, strength and return to normal
                    activities. The goal is to restore your normal shoulder movement without pain. Call us today to
                    discover how we can quickly resolve your shoulder pain and get you back to the activities you love.
                </p>
