                <h5>Rotator Cuff Injuries</h5>
                <p>
                    The rotator cuff is a group of muscles surrounding the shoulder. They are made up of the
                    supraspinatus, infraspinatus, sub–scapularis and teres minor muscles. While these muscles are small
                    and not that powerful, they play a critical role in how the shoulder moves. Without your rotator
                    cuff, you would not be able to lift your arm very far from your side. The job of the rotator cuff is
                    to guide the direction of the humerus head down and spin so that it clears the bony shelf above the
                    socket called the acromion. When the rotator cuff is weak, the head of the humerus rides up slamming
                    into the acromion above it.</p>

                <p>The rotator cuff takes a lot of abuse over a lifetime and tearing is very common as we age. Many
                    factors can lead to injury to the rotator cuff such as sports injuries, falls onto the shoulder or
                    arm, repeat lifting or movements, poor posture or heavy lifting in abnormal positions.</p>

                <p>Rotator cuff tears can be minor, causing pain and inflammation, or major which can require surgery.
                    With surgery, depending on a variety factors such as the quality of the tissue, the extent of the
                    tear and other health factors, the recovery can take between 3-6 months. Physical therapy is a very
                    important part of preparing for surgery and rehabilitating after surgery.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Often, with small tears, physical therapy can dramatically reduce pain, improve function and allow
                    you to lead an active lifestyle without any problems. Our physical therapists will work with you to
                    improve your shoulder range of motion, restore proper joint mobility, reduce inflammation, relieve
                    pain and improve the strength of your rotator cuff.
                </p>
                <p>
                    If surgery is needed, we work closely with your physician to follow his/her protocol on
                    rehabilitation. The beginning of therapy focuses on reducing pain, education on protection of the
                    surgery repair, and maintaining a certain range of motion. Over time, as your physician protocol
                    allows, range of motion is increased, joint mobility is restored and finally very gentle
                    strengthening is begun.
                </p>
                <p>
                    Towards the end of your physical therapy further strengthening and complete range of motion will be
                    attained for your shoulder. All of this is coordinated per the instructions of your physician and we
                    ensure thorough communication with your physician at all times. Call us today to learn more about
                    how we help your rotator cuff perform better and relieve your shoulder pain.
