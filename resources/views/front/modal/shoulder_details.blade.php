                <h5>What is Frozen Shoulder?</h5>
                <p>
                    Frozen shoulder is a painful shoulder joint condition. In medical terminology it is called “adhesive
                    capsulitis”. How frozen shoulder exactly begins is still a bit of a mystery, however, it typically
                    occurs after a trauma or repetitive injury to the shoulder. Women in the pre and post–menopausal age
                    range are more likely to experience frozen shoulder, however men can also experience frozen
                    shoulder.
                </p>
                <p>With frozen shoulder, the thick capsule of tissue that surrounds the shoulder joint begins to
                    experience chronic inflammation. The body begins a cycle of inflammation and scarring that causes
                    the capsule around the shoulder joint to contract and become limited in its flexibility. This causes
                    very painful range of motion in the shoulder when trying to move the arm.</p>
                <p>At the beginning of frozen shoulder it is very painful and range of motion becomes limited. This can
                    be around 4–8 weeks in duration. After that, motion is very limited in the shoulder, but often not
                    as painful. Depending on the severity of the condition, it can take sometimes up to a year to
                    resolve and improve range of motion.</p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy can make a big difference in shortening the time it takes to recover from frozen
                    shoulder (adhesive capsulitis). By coming to therapy as early as possible, the inflammatory cycle
                    can be significantly reduced, limiting scarring and contraction of the shoulder capsule. In turn,
                    physical therapy, along with medication can limit the pain and ensure a faster recovery.
                </p>
                <p>
                    Our physical therapists work with many frozen shoulder patients to reduce pain quickly and restore
                    range of motion to the shoulder. Physical therapy treatments focus on hands–on therapy and
                    specialized exercises to maintain as much range of motion as possible during the inflammatory phase
                    of frozen shoulder. During the “thawing” phase we work with you to improve range of motion in your
                    shoulder and restore strength.
                </p>
                <p>
                    It can take quite awhile for the motion to be restored to the shoulder, but by coming to physical
                    therapy, we can help you recover as quickly as possible. Call us today to find out how we can help
                    you relieve your frozen shoulder pain!
                </p>
