                <h5>What is Bursitis / Tendonitis?</h5>
                <p>


                    The ending of the word “itis” is defined as inflammation. Therefore, bursitis is inflammation of a
                    bursa and tendonitis is inflammation of a tendon. A bursa is a fluid filled sac that sits between
                    muscles or tissues to cushion and reduce friction. In the shoulder there is a rather large bursa
                    between the deltoid muscle and joint. This is called the sub-deltoid bursa. This bursa can often
                    become inflamed due to abnormal joint movements, poor posture and weakness of the surrounding
                    musculature. This causes strain to the tissues and excessive friction on the bursa. People tend to
                    feel pain with movement and especially movement out to the side or reaching behind them.</p>

                <p>Tendons connect muscles to bones. In the shoulder common areas for tendonitis are in the rotator cuff
                    tendons (supraspinatus, infraspinatus, teres minor and subscapularis) and bicep tendons. Pain can be
                    felt deep in the shoulder or in the front of the shoulder. Pain is usually felt as a sharp, catching
                    sensation with certain movements.
                </p>

