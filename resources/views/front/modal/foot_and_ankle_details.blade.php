                <h5>Difficulty walking</h5>
                <p>
                    It takes us at least 12 months as a baby to learn the fundamentals of walking. It takes even longer
                    to learn how to walk properly and eventually run. Walking is very complex and requires good balance,
                    the ability to know where your joints are in space (proprioception), the ability to know how your
                    joints are moving (kinesthesia), good range of motion and strength. <br>

                    As we age, with declining activity, or after an injury, walking can become difficult. With previous
                    injuries or pain in the knee or hip, our walking pattern can change leaving us with a limp or even
                    worse, back pain. Changes in posture can also be responsible for changes in walking patterns. <br>

                    When walking patterns change, abnormal stresses and strains with everyday activities can be
                    transmitted to areas it shouldn’t. For example, if you have knee pain and you begin to limp, the
                    other hip and your spine now have to take double the weight. This can lead to pain and dysfunction
                    in those areas also. The good news is that if you have difficulty walking, you can be helped.
                    Physical therapists are the experts uniquely trained to do so.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    One of the main specialties of physical therapy is helping people to walk normally. This takes a
                    thorough evaluation of your range of motion, strength, walking patterns, balance and coordination.
                    By discovering in what area you have difficulties we can paint a picture of why your walking is not
                    as it should be. <br>

                    We then coordinate a treatment plan that will address your range of motion, pain, coordination,
                    balance and strength. The end result is the ability to walk without the need of an assistive device
                    such as a cane or walker, safely and smoothly. Call us today to discover how we can help you walk
                    better!
