                <h5>What is Achilles Tendonitis?</h5>
                <p>
                    Achilles tendonitis is an inflammation of the Achilles tendon that attaches your calf muscle to the
                    back of your heel. Achilles tendonitis is typically caused by tightness in the hamstrings and calf
                    muscles. In addition, limitations in ankle and foot range of motion can alter your walking pattern,
                    putting excessive strain on the Achilles tendon, causing tendonitis. <br>

                    Typically, the back of the ankle and heel is very painful to touch, especially after prolonged
                    walking. Rest tends to help Achilles tendonitis while walking aggravates it.
                </p>

                <h5>
