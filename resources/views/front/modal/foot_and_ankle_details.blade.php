                <h5>About Ankle instability</h5>
                <p>Ankle instability typically occurs after the ligaments in the ankle have stretched out, losing the
                    normal stability of the bones in the ankle and foot. This can be a result of an injury or occur over
                    a long period of time from poor foot positioning, bad shoe wear or genetic predisposition. <br>

                    When there is ankle instability the probability of further ankle sprains increases significantly.
                    While the ligaments themselves may not be able to shorten back to normal, the muscles supporting the
                    ankle can be strengthened to take up the slack. This compensation allows for better stability in the
                    ankle and foot with walking, running and sports. Bracing may also be needed depending on the
                    severity of the instability.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Our physical therapists work with different physicians, including podiatrists to rehabilitate ankle
                    and foot injuries. By assessing your ankle and foot joint movements we can determine what areas need
                    to be strengthened bringing support and stability to your ankle. <br>

