                <h5>What is Plantar Fasciitis?</h5>
                <p>Plantar fasciitis refers to inflammation of the plantar fascia, which is a thick band of tissue on
                    the underside of the foot, connecting from the heel to the ball of the foot. Plantar fasciitis
                    typically occurs from tightening of the tissues under the foot and even further up in the legs. In
                    addition, changes in the shape of the foot, such as fallen arches or changes in shoe wear can affect
                    plantar fasciitis. Furthermore, with changes in walking posture for a variety of reasons, the strain
                    on the plantar fascia can increase significantly. <br>

                    Plantar fasciitis is commonly felt as burning or sharp pain when standing on the affected leg. This
                    typically occurs first thing in the morning or with initial standing after sitting for a prolonged
                    period. Inflammation builds up during the day in the fascia, and may begin to hurt after prolonged
                    walking. As you sit or sleep the inflammation thickens and the plantar fascia stiffens. When you go
                    to step on the foot, the inflamed and stiff tissue stretches causing pain. Pain may be felt mostly
                    in the heel, but can extend along the bottom of the foot.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is very beneficial in treating plantar fasciitis. At first we evaluate your
                    movement, posture, walking pattern, ankle and foot mobility. Our gentle hands-on therapy is great
                    for soothing the irritated tissue in the foot and also improving range of motion in the foot and
                    ankle joints. <br>

                    Modalities, such as ultrasound and ice may be used to reduce swelling and pain quickly. Our goal is
                    to reduce your pain quickly and strengthen your feet for long-term results. Call us today to
                    discover how we can help resolve your plantar fasciitis pain and get you quickly back on your feet!
                </p>
