                <h5>Ankle Sprain / Strain</h5>
                <p>
                    Ankle sprains are very common injuries and typically occur from improper footwear on uneven terrain.
                    Most often, sprains occur from sports or trips over objects that were unseen. Sprains refer to
                    injuries of the ligaments (connect bone to bone) and strains refer to injuries of the muscles or
                    tendons (connect muscle to bone). Sprains and strains occur from quick over-stretching of the
                    tissues causing micro-tearing and subsequent injury. Swelling begins as part of the inflammation
                    process, causing pain and difficulty with movement. <br>
                    The first step in treating sprains or strains in the ankle is to rest, ice and elevate it. Common
                    sprains in the ankle occur on the outside of the ankle where 3 main ligaments attach. There are
                    different levels of sprain or strain from mild to severe. With severe limitations in movement you
                    should see your physical therapist right away. In some cases, the tearing can be complete and even
                    be in need of surgical repair.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    In most cases, physical therapy can effectively help you recover from a sprain or strain. We first
                    evaluate the injured area to determine the extent of the injury and ensure that the ligaments or
                    tendons are still intact. We then formulate a treatment plan that will quickly relieve your
                    swelling, pain and begin restoring range of motion. <br>

                    The goal of physical therapy is to restore your normal range of motion and eventually restore normal
                    strength. If you participate in sports or are very active, we will work closely with you to make
                    sure that you fully recover and can participate in those activities you love to do. Call us today to
                    discover how we can effectively treat your ankle sprain or strain.
