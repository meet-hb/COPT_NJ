                    therapists will evaluate your leg, ankle and foot mobility, range of motion, strength, balance and
                    walking patterns. This will help us discover the root cause of your pain and resolve that for long
                    term results. <br>

                    We focus on treatments to resolve your pain quickly, improve your joint mobility and strengthen
                    muscles as needed. The result is the ability to walk and run without pain in the back of your heel
