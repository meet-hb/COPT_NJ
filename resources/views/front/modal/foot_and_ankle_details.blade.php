                <h5>Ankle and Foot Fractures</h5>
                <p>
                    There are many bones that can be fractured in the ankle and foot. Typical fractures that occur are
                    to the fibula bone (avulsion fracture), metatarsal bone fractures and calcaneal fractures. Fractures
                    are typically immobilized with casting or bracing. In severe cases surgery may be required to align
                    and stabilize the bone with pins or plates. <br>

                    After being immobile for a period of time whether from casting or surgery, the ankle and foot joints
                    will become quite stiff. This loss of motion in the joint also causes discomfort and results in lost
                    muscle strength in the ankle and foot.
                </p>

                <h5>
                    How physical therapy helps
                </h5>
                <p>
                    Physical therapy is very important in the rehabilitative process to help you regain normal range of
                    motion, reduce swelling, resolve pain and regain function of your ankle and foot. <br>

                    Physical therapy is gentle and will help you quickly resolve your pain while working with your
                    doctor to follow protocol and restore your function. Improvement in range of motion, strength,
                    stability, walking and running will occur. With physical therapy, you can make a complete recovery
